        int row = table.getSelectedRow();

        Penyewa pw = new Penyewa();
        pw = pw.getById(Integer.parseInt(model.getValueAt(row, 0).toString()));

        txtIdPenyewa.setText(String.valueOf(pw.getIdPenyewa()));
