    public void tampilkanData() {
        String[] kolom = {"ID Penyewa", "No Ktp", "Nama", "Alamat", "No Telp"};
        ArrayList<Penyewa> list = new Penyewa().getAll();
        Object rowData[] = new Object[5];

        table.setModel(new DefaultTableModel(new Object[][]{}, kolom));

        for (int i = 0; i < list.size(); i++) {
            rowData[0] = list.get(i).getIdPenyewa();
            rowData[1] = list.get(i).getNoKtp();
            rowData[2] = list.get(i).getNama();
            rowData[3] = list.get(i).getAlamat();
            rowData[4] = list.get(i).getNoTelp();

            ((DefaultTableModel) table.getModel()).addRow(rowData);
        }
    }
    
    public void cari(String keyword) {
         String[] kolom = {"ID Penyewa", "No Ktp", "Nama", "Alamat", "No Telp"};
        ArrayList<Penyewa> list = new Penyewa().search(keyword);
        Object rowData[] = new Object[5];

        table.setModel(new DefaultTableModel(new Object[][]{}, kolom));

        for (Penyewa pw : list) {
            rowData[0] = pw.getIdPenyewa();
            rowData[1] = pw.getNoKtp();
            rowData[2] = pw.getNama();
            rowData[3] = pw.getAlamat();
            rowData[4] = pw.getNoTelp();

            ((DefaultTableModel) table.getModel()).addRow(rowData);
        }
    }

