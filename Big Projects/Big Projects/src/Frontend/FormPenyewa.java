    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        Penyewa pw = new Penyewa();
        pw.setIdPenyewa(Integer.parseInt(txtIdPenyewa.getText()));
        pw.setNoKtp(Integer.parseInt(txtNoKtp.getText()));
        pw.setNama(txtNama.getText());
        pw.setAlamat(txtAlamat.getText());
        pw.setNoTelp(txtNoTelp.getText());
        pw.save();
        txtIdPenyewa.setText(Integer.toString(pw.getIdPenyewa()));
        tampilkanData();
    }//GEN-LAST:event_btnSimpanActionPerformed

