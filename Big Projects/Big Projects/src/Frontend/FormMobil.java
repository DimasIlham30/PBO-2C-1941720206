    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if (cmbJenis.getSelectedIndex() == 0) {
            MobilKeluarga mk = new MobilKeluarga();
            mk.setIdMobil(Integer.parseInt(txtidmobil.getText()));
            mk.setJenis((JenisMobil) cmbJenis.getSelectedItem());
            mk.setNopol(txtnopol.getText());
            mk.setMerkMobil(txtmerk.getText());
            mk.setWarnaMobil(txtwarna.getText());
            mk.setNamaMobil(txtnama.getText());
            mk.save();
            txtidmobil.setText(Integer.toString(mk.getIdMobil()));
            tampilkanData();
        } else {
            MobilPickUp mp = new MobilPickUp();
            mp.setIdPickUp(Integer.parseInt(txtidmobil.getText()));
            mp.setJenis((JenisMobil) cmbJenis.getSelectedItem());
            mp.setNopol(txtnopol.getText());
            mp.setMerkPickUp(txtmerk.getText());
            mp.setWarnaPickUp(txtwarna.getText());
            mp.setNamaPickUp(txtnama.getText());
            mp.save();
            txtidmobil.setText(Integer.toString(mp.getIdPickUp()));
            tampilkanData();
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

