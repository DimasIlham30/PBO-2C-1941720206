    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        int row = table.getSelectedRow();

        if (row == 0) {
            MobilKeluarga mk = new MobilKeluarga().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
            mk.delete();
            kosongkanForm();
            tampilkanData();
        } else {
            MobilPickUp mp = new MobilPickUp().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
            mp.delete();
            kosongkanForm();
            tampilkanData();
        }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        int row = table.getSelectedRow();

        if (row == 0) {
            MobilKeluarga mk = new MobilKeluarga();
            mk = mk.getById(Integer.parseInt(model.getValueAt(row, 0).toString()));

            txtidmobil.setText(String.valueOf(mk.getIdMobil()));
            cmbJenis.getModel().setSelectedItem(mk.getJenis().getJenis());
            txtnopol.setText(mk.getNopol());
            txtmerk.setText(mk.getMerkMobil());
            txtwarna.setText(mk.getWarnaMobil());
            txtnama.setText(mk.getNamaMobil());
        } else {
            MobilPickUp mp = new MobilPickUp();
            mp = mp.getById(Integer.parseInt(model.getValueAt(row, 0).toString()));

            txtidmobil.setText(String.valueOf(mp.getIdPickUp()));
            cmbJenis.getModel().setSelectedItem(mp.getJenis().getJenis());
            txtnopol.setText(mp.getNopol());
            txtmerk.setText(mp.getMerkPickUp());
            txtwarna.setText(mp.getWarnaPickUp());
            txtnama.setText(mp.getNamaPickUp());
        }
    }//GEN-LAST:event_tableMouseClicked

